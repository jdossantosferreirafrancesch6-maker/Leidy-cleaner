/**
 * Retrieve a translated message.
 *
 * @param key Translation key
 * @param locale Locale code (defaults to env DEFAULT_LOCALE or pt-BR)
 */
export function t(key: string, locale?: Locale) {
  const loc = (locale || (process.env.DEFAULT_LOCALE as Locale) || 'pt-BR') as Locale;
  // direct lookup by key
  if (translations[loc] && translations[loc][key]) {
    return translations[loc][key];
  }
  if (translations['en'] && translations['en'][key]) {
    // found english key, return localized version or english
    return translations[loc]?.[key] || translations['en'][key];
  }
  // if key looks like english message, attempt reverse lookup
  for (const k in translations['en']) {
    if (translations['en'][k] === key) {
      return translations[loc]?.[k] || translations['en'][k];
    }
  }
  return key;
}
